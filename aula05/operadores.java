public class operadores {
    public static void main(String[] args) {
        int x = 10;
        int y = 3;

    System.out.println(x + y); // resulta em 13
    System.out.println(x - y); // resulta em 7
    System.out.println(x * y); // resulta em 30
    System.out.println(x / y); // resulta em 3
    System.out.println(x % y); // resulta em 1

    int z = 5;
    ++z;
    System.out.println(z); // = 6
    --z;
    System.out.println(z); // = 4
    
    int a = 10;
    int b = 7;
    
    System.out.println(a / b);   // Integer division, result is 3

    double c = 10.0d;
    double d = 3.0d;
    System.out.println(c / d);

    }
}
